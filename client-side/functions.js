function findEmoticons(msg) {
    msg = escape(msg).replace(/%u/g, '\\u');
    var emoticons = msg.match(PATTERN);
    if (msg == "" || msg == null || emoticons == null)
        return unescape(msg);

    emoticons.forEach(function (code) {
        var uni = code;
        code = getCharAsDecimalUTF8(uni);
        if (EMOJI[code])
            msg = msg.replace(uni, '<img src="/client-side/' + EMOJI[code] + '"/>');
    });
    return unescape(msg);
}

function getCharAsDecimalUTF8(ot) {
    var getUTF8 = ot;
    return "&#" + parseInt(getUTF8.substring(getUTF8.match(/\\u/) !== null ? 2 : 1), 16);
}